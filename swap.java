class swap 
{
    public static void main (String args[])
    {
        int a,b,temp;
        a=6;
        b=8;
        System.out.println("Before Swapping a="+a+" and b="+b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("After Swapping a="+a+" and b="+b);
    }
}